// object literal { }
// key: value
// key must be string.
// value can be any data type
const person = {
  firstName: "Ahmed",
  lastName: "Mohamed",
  age: 25,
  skills: ["HTML", "CSS", "JS", "REACT"],
  isMarried: false,
  "his hobbies": ["sports", "reading", "coding"],
};

// accessing Object keys

person.firstName; // dot notation
person["firstName"]; // bracket notation

